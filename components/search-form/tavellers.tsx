import { Menu, Transition } from "@headlessui/react";
import { Fragment, useEffect, useRef, useState } from "react";
import { ArrowDown2 } from "iconsax-react";

export const travellers: Array<Array<string>> = [
  ["adults", "12 years & above"],
  ["children", "From 5 to under 12"],
  ["kids", "From 2 to under 5"],
  ["infants", "Under 2 years"],
];

export const Counter = () => {
  const [count, setcount] = useState<number>(0);

  return (
    <div className="md:p-2 inline-block bg-white md:border border-gray-200 md:rounded-lg">
      <div className="flex items-center gap-x-1.5">
        <button
          type="button"
          onClick={() => {
            setcount(Math.max(count - 1, 0));
          }}
          className="w-6 h-6 inline-flex justify-center items-center gap-x-2 text-sm font-medium rounded-md border border-gray-200 bg-white text-gray-800 shadow-sm hover:bg-gray-50 disabled:opacity-50 disabled:pointer-events-none"
        >
          <svg
            className="flex-shrink-0 w-4 h-4"
            xmlns="http://www.w3.org/2000/svg"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth={2}
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <path d="M5 12h14" />
          </svg>
        </button>

        <input
          className="p-0 w-6 bg-transparent border-0 text-gray-800 text-center focus:ring-0"
          type="text"
          value={count}
          onChange={(e) => {
            setcount(parseInt(e.target.value));
          }}
        />
        <button
          type="button"
          onClick={() => {
            setcount(count + 1);
          }}
          className="w-6 h-6 inline-flex justify-center items-center gap-x-2 text-sm font-medium rounded-md border border-gray-200 bg-white text-gray-800 shadow-sm hover:bg-gray-50 disabled:opacity-50 disabled:pointer-events-none"
        >
          <svg
            className="flex-shrink-0 w-4 h-4"
            xmlns="http://www.w3.org/2000/svg"
            viewBox="0 0 24 24"
            fill="none"
            stroke="currentColor"
            strokeWidth={2}
            strokeLinecap="round"
            strokeLinejoin="round"
          >
            <path d="M5 12h14" />
            <path d="M12 5v14" />
          </svg>
        </button>
      </div>
    </div>
  );
};

export type travellersObject = {
  title: string;
  desc: string;
};

export default function Travellers() {
  const data = travellers.map(
    (e): travellersObject => ({ title: e[0], desc: e[1] })
  );

  return (
    <div className="w-full sm:w-44 md:w-48">
      <Menu as="div" className="w-full relative inline-block text-left">
        <Menu.Button className="relative w-full cursor-default rounded-lg bg-green-500/10 h-9 md:h-11 pl-3 md:pl-4 pr-10 text-left shadow-md+ focus:outline-none focus-visible:border-indigo-500 focus-visible:ring-2 focus-visible:ring-white/75 focus-visible:ring-offset-2 focus-visible:ring-offset-orange-300 sm:text-sm">
          <span className="block text-green-500 font-medium text-sm">
            Travellers
          </span>
          <span className="pointer-events-none absolute inset-y-0 right-0 flex items-center pr-2">
            <ArrowDown2 className="w-4 h-4 text-green-500" />
          </span>
        </Menu.Button>
        <Transition
          as={Fragment}
          enter="transition ease-out duration-100"
          enterFrom="transform opacity-0 scale-95"
          enterTo="transform opacity-100 scale-100"
          leave="transition ease-in duration-75"
          leaveFrom="transform opacity-100 scale-100"
          leaveTo="transform opacity-0 scale-95"
        >
          <Menu.Items className="absolute left-0 lg:right-0 mt-2 w-72 origin-top-right divide-y divide-gray-100 rounded-md bg-white shadow-lg ring-1 ring-black/5 focus:outline-none z-[2]">
            {data.map((item: travellersObject, index: number) => (
              <div
                className="p-2 md:p-4 flex items-center justify-between"
                key={index}
              >
                <div className="text-sm text-gray-700">
                  <h3 className="font-semibold capitalize">{item.title}</h3>
                  <p className="text-sm">{item.desc}</p>
                </div>
                <Counter />
              </div>
            ))}
          </Menu.Items>
        </Transition>
      </Menu>
    </div>
  );
}

